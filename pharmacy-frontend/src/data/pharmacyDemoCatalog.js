export function buildInitialDemoProducts() {
  const out = [];
  let id = 1;

  const add = (fields) => {
    out.push(row(id++, fields));
  };

  const paraVariants = [
    { vl: "100mg/5ml شراب أطفال", st: "bottle", cat: "أطفال", pr: 9, q: 40, min: 15 },
    { vl: "120mg أقراص", st: "strip", cat: "أطفال", pr: 7, q: 55, min: 20 },
    { vl: "125mg لبوس", st: "strip", cat: "أطفال", pr: 8, q: 48, min: 18 },
    { vl: "250mg أقراص مضغ", st: "strip", cat: "أطفال", pr: 10, q: 60, min: 22 },
    { vl: "500mg أقراص", st: "strip", cat: "مسكنات", pr: 12, q: 200, min: 50 },
    { vl: "500mg فوار", st: "box", cat: "مسكنات", pr: 14, q: 90, min: 30 },
    { vl: "650mg أقراص", st: "strip", cat: "مسكنات", pr: 13, q: 75, min: 25 },
    { vl: "1000mg أقراص", st: "strip", cat: "مسكنات", pr: 15, q: 65, min: 22 },
    { vl: "1000mg شراب", st: "bottle", cat: "مسكنات", pr: 16, q: 42, min: 15 },
    { vl: "80mg/ml قطرة (عيّنة)", st: "bottle", cat: "أطفال", pr: 11, q: 28, min: 10 },
  ];
  const paraPatientInfo = {
    usageHowTo:
      "مسكن للألم وخافض للحرارة. يُبلع مع كوب ماء، أو يُذاب الفوار في ماء حسب النشرة. للشراب: رجّ العبوة واستخدم المِلعقة المرفقة.",
    usageFrequency:
      "بالغون: غالباً قرص كل 4–6 ساعات عند الحاجة دون تجاوز الجرعة القصوى اليومية الموضحة على العبوة. للأطفال: حسب الوزن والعمر — راجع الصيدلي.",
    usageTips:
      "لا تدمج مع أدوية أخرى تحتوي باراسيتامول. احذر الجرعة القصوى اليومية. استشر الطبيب عند الحمل أو أمراض الكبد.",
  };
  paraVariants.forEach((v, i) => {
    add({
      name: "باراسيتامول",
      variantLabel: v.vl,
      category: v.cat,
      saleType: v.st,
      qty: v.q,
      min: v.min,
      price: v.pr,
      saleOptions: paracetamolSaleOptions(`pa-${i}`),
      ...paraPatientInfo,
    });
  });

  const saleTypesCycle = ["strip", "strip", "box", "bottle"];
  /** إجمالي 100 صنف: متغيرات الباراسيتامول + أصناف مولَّدة موزّعة بالتناوب على DEMO_CATEGORY_NAMES */
  const TARGET = 100;
  for (let k = out.length; k < TARGET; k++) {
    const j = k - paraVariants.length;
    const stem = DEMO_DRUG_STEMS[j % DEMO_DRUG_STEMS.length];
    const cat = DEMO_CATEGORY_NAMES[j % DEMO_CATEGORY_NAMES.length];
    const st = saleTypesCycle[j % saleTypesCycle.length];
    const mg = 25 + ((j * 37) % 975);
    let variantLabel;
    if (st === "bottle") {
      variantLabel = j % 2 === 0 ? `${mg}mg/5ml شراب` : `${mg}mg/ml قطرة`;
    } else if (st === "box") {
      variantLabel = `${mg}mg علبة`;
    } else {
      variantLabel = j % 3 === 0 ? `${mg}mg أقراص` : j % 3 === 1 ? `${mg}mg كبسول` : `${mg}mg شريط`;
    }
    const price = roundOneDecimalLocal(5 + (j * 13) % 92 + (j % 3) * 0.5);
    const qty = 28 + ((j * 11) % 160);
    const min = 8 + ((j * 5) % 35);
    add({
      name: stem,
      variantLabel,
      category: cat,
      saleType: st,
      qty,
      min,
      price,
      barcode: `781${String(1000000 + k).padStart(10, "0")}`,
      ...(j % 40 === 0
        ? {
            usageHowTo: "يُستخدم وفق وصفة الطبيب أو إرشاد الصيدلي ونشرة الدواء.",
            usageFrequency: "الجرعة والتكرار يحددهما الطبيب؛ لا تتجاوز المدة أو الكمية دون استشارة.",
            usageTips: "اقرأ النشرة؛ أبلغ الطبيب عن الأدوية الأخرى والحمل.",
          }
        : {}),
    });
  }

  return out;
}

function roundOneDecimalLocal(n) {
  return Math.round(Number(n) * 10) / 10;
}
